public abstract class Animal {

    String name;
    boolean isFantastic;
    boolean canFly;
    boolean canRun;

    public Animal(String name, boolean isFantastic, boolean canFly, boolean canRun) {
        this.name = name;
        this.isFantastic = isFantastic;
        this.canFly = canFly;
        this.canRun = canRun;
    }

}
